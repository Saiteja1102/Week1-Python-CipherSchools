# string methods 

name = "Sai Teja"

# 1. len() function - gives the length of the string
#                      spaces also get counted
print(len(name))

# 2. lower() function - converts capital letters to small
print(name.lower())

# 3. upper() function - converts small letters to capital letters
print(name.upper())

# 4. title() function - converts first letter in capital and others in small
print(name.title())

# 5. count() function - counts the number of letters given
print(name.count("a"))
