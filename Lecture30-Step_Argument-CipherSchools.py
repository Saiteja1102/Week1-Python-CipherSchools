# syntax - [start argument : stop argument + 1 : step]
# step - jumps the number of times

print("Sai Teja"[2:5])
print("Sai Teja"[0:5:1])
print("Sai Teja"[0:5:2])
print("Sai Teja"[0:5:3])
print("Sai Teja"[0::2])
print("Sai Teja"[::2])

# backwards step
print("Sai Teja"[5::-1]) # reverse the string
print("Sai Teja"[-1::-1]) #### very imp trick
